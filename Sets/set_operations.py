A = {1, 2, 3, 4}
B = {3, 4, 5, 6}

union_set = A | B       
intersection_set = A & B 
difference_set = A - B   
sym_diff_set = A ^ B     

print("Union Set: ",union_set)
print("Intersection Set: ",intersection_set)
print("Difference Set: ",difference_set)
print("Symmetric Difference Set: ",sym_diff_set)
